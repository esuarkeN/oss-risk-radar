import { mkdirSync } from "node:fs";
import path from "node:path";
import { spawn } from "node:child_process";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..", "..");
const outputPath = "mltraining/scoring/constraints-py314.txt";
const pythonImage = process.env.PYTHON_LOCK_IMAGE ?? "python:3.14-slim";

function runCommand(command, args) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: repoRoot,
      env: process.env,
      stdio: "inherit",
      shell: false,
    });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) {
        resolve();
        return;
      }
      reject(new Error(`${command} exited with code ${code}`));
    });
  });
}

const lockScript = String.raw`
set -eu
python -m venv /tmp/scoring-lock
. /tmp/scoring-lock/bin/activate
python -m pip install --upgrade pip
python -m pip install "/workspace/mltraining/scoring[dev,notebook]"
python -m pip freeze --all > /tmp/scoring-freeze.txt
python - <<'PY'
from pathlib import Path

target = Path("/workspace/mltraining/scoring/constraints-py314.txt")
raw_lines = Path("/tmp/scoring-freeze.txt").read_text(encoding="utf-8").splitlines()
excluded_prefixes = ("oss-risk-radar-scoring @", "oss-risk-radar-scoring==")
lines = []
seen = set()
for raw in raw_lines:
    line = raw.strip()
    lowered = line.lower()
    if not line or lowered.startswith(excluded_prefixes):
        continue
    if lowered in seen:
        continue
    seen.add(lowered)
    lines.append(line)

lines.sort(key=str.lower)
target.write_text(
    "# Generated by npm run python:lock:scoring using python:3.14-slim.\n"
    "# Do not edit manually.\n"
    + "\n".join(lines)
    + "\n",
    encoding="utf-8",
)
print(f"wrote {target}")
PY
`;

try {
  mkdirSync(path.dirname(path.join(repoRoot, outputPath)), { recursive: true });
  await runCommand("docker", [
    "run",
    "--rm",
    "--volume",
    `${repoRoot}:/workspace`,
    "--workdir",
    "/workspace",
    pythonImage,
    "sh",
    "-lc",
    lockScript,
  ]);
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}
